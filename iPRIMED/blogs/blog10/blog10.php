                            of Unlocking
                            HTD with
                            SkillTech! With a little help from my friends in the policy/program design space, this will
                            help achieve
                            scale with
                            quality and that too very very rapidly! My only submission to the policy makers/program
                            designers is to be
                            aware that
                            while Tata Memorial Hospital in Mumbai is probably the best placed to cure Cancer, the
                            success rates there
                            also is
                            wanting..</p>









