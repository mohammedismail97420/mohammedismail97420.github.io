                        <a href="#">The Unplaced expectation around Placements!</a>
                    </div>
                    <div class="bdate">
                        <p style="display: inline-block;">Posted on</p>
                        <a href="#" rel="bdate">November 21, 2018</a>
                        </span>
                    </div>
                    <div class="bcont">
                        <p>You need skill to work efficiently and effectively. Skill has relevance only in the context
                            of work/task
                            and by
                            itself has little/no meaning. So, any skilling initiative should be tied to a job and should
                            equip the
                            trainee well
                            enough to help them land the job that they were skilled for - makes sense, right? Perfectly
                            logical - am
                            sure, you
                            couldn't disagree. None of us do.</p>

                        <p>Now using this/similar logic, policy makers and program designers have set placement targets
                            where 50% to
                            70% of all
                            trainees undergoing skill development programs should be placed. Again, makes sense - if
                            even half of the
                            people who
                            under go this kind of training do not get into jobs, then what is the point of these
                            skilling programs?
                            Apart from
                            this, I am not sure if there was prior data and analysis used to arrive at these lofty
                            targets - will be
                            happy to hear
                            about it from anyone who maybe privy to the same!! I have been looking for the basis on
                            which these
                            targets were set
                            for quite some time...</p>

                        <p>So, things should be hunky dory in the skills space! However, that's not the case as actual
                            placement
                            achieved across
                            programs is quite low ( 10% to 20%)
                            (https://www.business-standard.com/article/economy-policy/why-india-s-skill-mission-has-failed-117090200098_1.html).
                            Why is this happening? Where are we going wrong?</p>

                        <p>While this is a complex issue and there are many variables that lead to successful outcomes,
                            for this
                            article, we'll
                            focus on the role of the Skill Provider in achieving the target percentages. Should the
                            Skills provider be
                            solely
                            responsible for the Placement targets? In my mind, the answer is a vehement no. It will be
                            good to
                            remember that the
                            Skill provider's strength is imparting skills and making transfer of learning happen and not
                            in making
                            placements
                            happen. Placements should happen - but, aren't the competencies required there different? If
                            not, then how
                            do we
                            explain the existence of an industry sub sector that focuses only on Placements?</p>

                        <p>It doesn't end here, for successful placements, one of the key ingredients is the quality and
                            intent of
                            trainees
                            sourced for the program - if sourcing is not right, then no matter what happens during the
                            training
                            interventions and
                            there on, during the placement stage; the placement numbers will be dismal! One can take the
                            horse to the
                            pond, but
                            can't make the horse drink, if it doesn't want to! Now, does this mean that the Skills
                            Provider should
                            also be an
                            expert in sourcing candidates? Policy makers/Corporates tend to think so!!</p>


                        <p>So, the job of the poor Skills Provider ,who already has a super tough job on its hands to
                            transform a
                            seemingly
                            "untransformable" set of people, becomes even tougher - pretty much setting it up for
                            failure! It would be
                            good to
                            remember that we are dealing with adults who are not academically inclined and do not have
                            high endurance
                            and where a
                            lot of fossilisation has already happened and unlearning, critical to learning anything new,
                            is difficult
                            to achieve!
                            And by the way, the Skills Provider has to achieve this working with an army of "not so"
                            able bodied
                            soldiers as the
                            Skilling sector doesn't attract the best and brightest professionals since it is not the
                            most happening
                            profession
                            from a societal stand point! Now, over and above that it has to worry about 2 very different
                            kind of
                            functions -
                            Sourcing & Placements! What are the odds of success? No points for guessing..</p>
                        <p>So, off the bat, these programs are designed to fail! A possible way out is to have
                            specialists operate
                            these 3 key
                            processes ( Sourcing, Skilling, Placement) in the Skills value Chain i.e. one company works
                            on one process
                            only. A
                            central/nodal team akin to a Program Management Office cutting across the 3 processes, is
                            responsible for
                            governance
                            of the eco system and optimises the same for scale and scope. Proper entry and exit criteria
                            needs to be
                            defined and
                            adhered to at every process level. Expecting one company to do everything is bound to fail
                            esp. if we are
                            looking at
                            large numbers!</p>

                        <p>Specifically, if we look at Placements, we have to consider both Demand & Supply side of the
                            equation, if
                            we have to
                            achieve high percentages. Typically, we have been focusing a lot on the supply side without
                            fully/optimally aligning
                            the supply to demand! A lot of times, this is not in the control of the Skills Provider as
                            they are
                            mandated to impart
                            skills training in areas where the demand maybe negligible/non existent and then expected to
                            achieve 70%
                            placement
                            rates! The choice of skills at times is driven by political/corporate agendas and at times
                            as a result of
                            sheer
                            "ignorance" of market and industry dynamics. The latter can be attributed to the whims and
                            fancies of a
                            few
                            individuals who go by their "gut feel" as they strongly relate to the dictum - "We are
                            feeling beings who
                            think and
                            not thinking beings who feel"!</p>

                        <div class="text-center mt-3 mb-3">
                            <img class="img-fluid w-75" src="1.jpg" alt="">
                        </div>

                        <p>Skilling programs under the CSR umbrella take an even more unplaced view on placements! By
                            definition/design, the
                            beneficiaries are from a lower socio economic strata and worse/paradoxically so, even less
                            inclined to
                            hard work.
                            Since input quality is not under control, the training/transformation process should be
                            intense, holistic
                            and over a
                            long duration to make up for the quality of input, if we were to expect good outcomes.
                            Taking the analogy
                            of a
                            hospital, they'd need to be in ICU and not in General Ward! However, what's happening is
                            just the reverse.
                            Limited
                            budgets are assigned to impact large numbers ( expenditure per candidate is significantly
                            lower as
                            compared to the
                            tightly coupled HTD model executed in controlled environments) and while the expectations
                            for placements
                            still stay in
                            the range of 50% to 70%! How can you win when an initiative is designed to fail?</p>

                        <p>Now, moving from today's tightly coupled mode ( all 3 processes run by one agency) to a
                            loosely coupled
                            mode with
                            strong governance will require structural changes and hence will require time. So, what
                            should be done in
                            the interim?
                        </p>

                        <p>Running the closed loop Source/Hire, Train, Onboard/Deploy HTD model is definitely a way out
                            in the
                            interim - please refer my earlier article -
                            https://www.linkedin.com/pulse/everyone-seems-talking-htd-hire-train-deploy-model-freshers-kumar/
                            The
                            numbers will be smaller but more definite and the percentages will be higher.</p>


