                    <div class="bname mb-3">
                        <a href="#">eLearning Content Services can take a cue from IT Services & not reinvent the
                            Wheel!</a>
                    </div>
                    <div class="bdate">
                        <p style="display: inline-block;">Posted on</p>
                        <a href="#" rel="bdate">December 29, 2018</a>
                        </span>
                    </div>
                    <div class="bcont">
                        <p>eLearning or Digital Learning has started to gain traction. Completely self-paced eLearning
                            is yet to
                            take off. There are ample variations of the blended/hybrid model – each claiming to be
                            better than the
                            others! These are good signs. Signs of evolution! What’s also evolving, albeit not as
                            rapidly, is
                            <b>eLearning
                                Content services (eLCS)</b>
                        </p>
                        <p>eLCS manages the entire lifecycle of content - conceptualize, design, develop, test, deploy
                            and
                            maintain/re-engineer/retire. Basically, eLCS powers eLearning and has a critical role in
                            making it
                            effective and efficient!</p>

                        <p>Today, eLCS lacks maturity. Fairly unorganized. Not in the radar of CXOs! No clear
                            understanding of
                            Process Maturity. Standardization issues. Owned by different departments in different
                            companies – most of
                            the times, part of a broader Learning & Development (L&D) organization, at times part of the
                            IT
                            organization or sometimes fits into the newly christened “CDO” (Chief Digital Officer) and
                            on rare
                            occasions stands by itself! Integration of work products is a major cause of failure. Cost
                            pressures don’t
                            seem to have hit eLCS yet as outsourcing is not mature and Offshoring is far away! Providers
                            are born
                            every day, and an equal number or more die every other day!</p>
                        <div class="text-center mt-3 mb-3">
                            <img class="img-fluid w-75" src="1.png" alt="">
                        </div>
                        <p>Notes of Déjà vu! All of the above would have been applicable to eLCS’s bigger brethren, the
                            IT Services
                            industry in the 1980s and early 1990s! And it doesn’t end here, eLCS has many other
                            similarities with the
                            IT Services industry:</p>
                        <ul class="ml-4">
                            <li>Akin to IT services, the role of “Business” is played by the Authors/Subject Matter
                                Experts (SMEs). In
                                the normal scheme of things, like the way IT services don't create the product/offering
                                (done by the
                                Business), the same way eLCS doesn’t create the raw/book content. In some occasions, the
                                roles merge –
                                however, that’s more of an exception than the norm!</li>
                            <li>Like IT services, eLCS has to understand the requirements from the authors/SMEs. This is
                                an area where
                                maximum drops
                                happen for both! IT services have created a specific role to handle this and minimize
                                the drops – the
                                role of Business
                                Analysts/Consultants! Not sure, if we have a (mature) parallel in the eLCS world.</li>
                            <li>Both follow a similar execution life cycle. However, their maturity of process/model
                                adoption varies
                                significantly. The leading IT services players live and breathe by the Consistency,
                                Repeatability,
                                Predictability mantra, the same is a distant cry in the world of eLCS.</li>
                            <li>Both work with multiple stakeholders, work break down structures, etc. One leverages
                                very mature
                                Program/Project Management processes, and the other relies on heroes and somehow manages
                                to get things
                                done (No points for guessing who’s who!)</li>
                            <li>Time to Market is an essential consideration for both. eLCS tends to have a slow time to
                                market esp.
                                for high
                                fidelity content (which is the norm these days!)</li>
                            <li>Availability of skilled/trained personnel is always an issue!</li>
                        </ul>
                        <p>However, eLCS is not exactly like IT services. There are aspects that are different</p>
                        <ul class="ml-4">
                            <li>
                                Level of imagination/creativity required is much higher
                            </li>
                            <li>
                                Whereas the output of IT services is mostly used for transactional purposes, like
                                automating the
                                ordering of a phone,
                                the output of eLCS is for the consumption of humans to learn/acquire skills; these can
                                be very different
                                things!
                            </li>
                            <li>
                                eLCS can be way more tedious than IT services resulting in monotony, errors, delayed
                                timelines, etc.
                            </li>
                            <li>For a P&L holder on the Corporate (non learning Company) side, unit costs are high but
                                as a percentage
                                much lower as compared to IT services. Though in the VICA world, where
                                products/offerings are fast
                                changing, the learning literature that goes with it needs to be changed rapidly as well
                                – resulting in
                                higher costs!</li>
                        </ul>
                        <p>However, the similarities are way more than the differences. It will be foolhardy, if eLCS,
                            doesn’t
                            incorporate the learnings and best practices from the way more mature IT services world.</p>

                        <p>Some key aspects to consider to enhance the maturity of eLCS:</p>

                        <ul class="ml-4">
                            <li><b>Bring in focus and specialization</b></li>
                            <ul style="list-style-type: circle;" class="ml-5">
                                <li>Intelligent work breakdown into core and non-core activities</li>

                                <li>All non-core activities to be outsourced/offshore to specialist third party
                                    providers. This will
                                    release the bandwidth of SMEs while bringing down costs. If a “follow the sun” model
                                    of resource
                                    deployment is used, then “Time to Market” would improve significantly!</li>

                                <li>Of course, the usage of reusable components, effective knowledge management, usage
                                    of Robotics
                                    Process Automation (RPA) will improve cost and time efficiency even further!</li>
                            </ul>
                            <li><b>Bring in structure</b></li>

                            <ul style="list-style-type: circle;" class="ml-5">
                                <li>Define a Process with well-defined Measures & Metrics. This could be modeled along
                                    industry-leading
                                    frameworks like CMMI-Dev with appropriate tailoring to align every organization's
                                    objectives, culture,
                                    and needs. Important aspects to incorporate:</li>
                                <ul style="list-style-type: square;" class="ml-5">
                                    <li>
                                        Process Architecture
                                    </li>
                                    <li>Work Classification Framework</li>
                                    <li>Workflow/Life Cycle Processes</li>
                                    <li>Management Processes</li>
                                </ul>
                                <li>Governance Framework to drive efficiency & effectiveness esp. when work is
                                    outsourced/offshored to
                                    third parties. Aspects to Consider</li>
                                <ul style="list-style-type: square;" class="ml-5">
                                    <li>Process Management</li>
                                    <li>Quality Assurance</li>
                                    <li>Vendor Management</li>
                                    <li>Knowledge Management & Reuse</li>
                                    <li>Enterprise Content Architecture Management</li>

                                </ul>
                            </ul>


                        </ul>
                        <p>These would take care of most of the failure points and issues in the eLCS world and propel
                            it on a
                            faster maturity
                            curve! With this, the ongoing Digital Revolution will get a tremendous boost. Reinventing
                            the Wheel is not
                            an option!
                        </p>


