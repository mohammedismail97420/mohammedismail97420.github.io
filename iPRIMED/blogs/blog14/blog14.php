                    <div class="bcont">
                        <p>Deja Vu times! Reminds me of the IT era, pre Agile!
                        </p>
                        <p>Throughout the 80s and 90s, IT’s slow pace was always an enigma for the business folks.
                            Business couldn’t
                            comprehend
                            why getting anything done on the It side took so much time and as tech became more and more
                            important,
                            something had
                            to be done to make it more nimble. Not sure, if lessons were drawn from how the other
                            “Enigma” code was
                            cracked during
                            World War II, but something similar happened. Heightened attention, rigorous analysis &
                            critical thinking
                            and not
                            taking “NO" for an answer, resulted in the inevitable – time to production cut down
                            significantly!</p>

                        <p>When the "iterative" process shaved off months from the classic waterfall SDLC, the common
                            wisdom was
                            that
                            things
                            couldn’t get better than this! Agile raised its head with "Extreme Programming” but mostly
                            stayed in the
                            fringes! And
                            then, Scrum changed everything…</p>

                        <p>L&D is at a similar stage. The VUCA ( Volatile, Uncertain. Complex and Ambiguous) world
                            requires
                            organizations (and
                            in turn employees) to be able to improvise in real time. Survival and growth imply that the
                            “Learn –
                            Unlearn
                            -
                            Relearn” cycle has to become shorter and shorter! Organizations can’t afford long
                            implementation cycles as
                            the
                            “context” for which the learning was required itself may change, thereby, jeopardising the
                            ROI for the
                            initiative!
                            Besides, the reducing attention span of the Digital natives and the Millennials is
                            witnessing an inverse
                            “Moore’s
                            Law”! All this necessitates changes in the learner as well as the learning provider
                            ecosystem:</p>

                        <p>An “Agile” approach similar to what has been practiced on the IT side has answers to most of
                            the
                            challenges
                            faced by L&D. L&D departments should consider adopting the same in dear earnest! Both, the
                            Demand” and the
                            “Supply” side need to be addressed:</p>

                        <p>Digital is making things much easier as boundaries of location, space and time are getting
                            blurred. This
                            needs to be
                            leveraged to the fullest. If the “Digital/Automation Maturity” of your organization is on
                            the lower side,
                            then you may
                            want to explore “Outsourcing” the non core functions like Content Production & Maintenance,
                            Testing,
                            porting
                            from one
                            platform to another etc. This will give you enough of a breather as you transition to an
                            Agile L&D
                            organization!
                            Offshoring ( using different time zones) can provide another booster shot by using extended
                            work days,
                            thereby
                            reducing the cycle time and ensuring safer outcomes! May not be agile in the real sense of
                            the term, but
                            could be a
                            strong crutch to lean on as you transition into the new world!</p>

                        <p>While the picture that I have painted is quite hunky dory, let me leave you with a caveat -
                            Agility in
                            the
                            L&D
                            organization is a necessary but insufficient condition for an organization to realize the
                            true benefits of
                            Agility.
                            For best outcomes, the entire organization i.e. all departments need to be agile, else the
                            weakest link in
                            the chain
                            will determine the strength of the chain!</p>
