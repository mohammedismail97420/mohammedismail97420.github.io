                        <p>One of the impediments to achieving Agility is the lack of an objective way of measuring L&D
                            Agility. And we all know, that what can't be measured can't be improved!
                        </p>
                        <p>So, here you go - we have created an Agility Index to help L&D ( or for that matter any
                            organisation that has to create & manage Digital Content) organisations on their improvement
                            journey. We have drawn parallels with a modern News Room, where things no longer happen only
                            in
                            a "linear" and planned manner. There is enough and more breaking news and crowd sourced news
                            (
                            one has to be careful of fake news!) being put on air where things happen in a more
                            "spatial"
                            manner.</p>

                        <p>To measure L&D Agility, we need to look at 2 key factors:</p>
                        <ol type="1" class="ml-4">
                            <li>Responsiveness in Planned Situations ( RPS)</li>
                            <li>Responsiveness in Unplanned Situation ( RUS)</li>
                        </ol>
                        <p>Typically, there is a tradeoff between the above two factors. </p>
                        <img class="img-fluid" src="1.png" alt="">
                        <p>An organisation which is highly responsive in planned situations (long cycle) finds it
                            difficult
                            to be responsive in ad hoc/unplanned situations and vice versa. The ability to be good in
                            both,
                            without violating the cardinal laws/guiding principles of learning/communication is critical
                            to
                            an organisation being Agile!</p>

                        <p>An organisation's responsiveness can be classified as Low, Medium, High depending on their
                            capability on the Process, Technology & People aspects to be able to be responsive in a
                            consistent, repeatable and predictable manner. Basis these classifications, there are 5
                            levels
                            on the Agility Index</p>
                        <div class="text-center mb-3 mt-3">
                            <img class="img-fluid w-75 text-center" src="2.png" alt="">

                        </div>
                        <p>Since technology has a huge play in driving Agility and esp. for the Unplanned situations,
                            reaching Level 5 may not be practical given where tech developments stand today! Level 4
                            should
                            be a good state to be in. In our experience most L&D organisations are at Level 2, some are
                            at
                            Level 1 and very few at Level 3. We haven't come across any that's at Level 4! A Level 4
                            organisation mirrors a well functioning modern News Room!</p>
                        <p>How do we determine Low/Medium/High in an objective manner? A little bit of arithmetic always
                            helps! Each of the key
                            aspects ( Process, Tech, people) is measured basis 5 key questions each ( total of 15
                            questions)
                            and then the
                            overall score is divided into 3 levels:</p>
                        <ul class="ml-4">
                            <li>Low: less than equal to 5</li>
                            <li>Medium: greater than 5 but less than equal to 10</li>
                            <li>High: greater than 10</li>
                        </ul>
