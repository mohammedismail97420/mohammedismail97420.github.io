                            make
                            sense? Does it fill a void? Will you be ok to use the same?</p>
                        <p>And how does an organisation move levels? It's a complex journey where we'll have to work
                            with the old horses - Process, Technology & People! Technology has a big role to play but,
                            one has to be careful so that Tech doesn't drive the process and things stay the other way
                            round! Again, happy to discuss offline unless you'd like to wait for my next article :)</p>
