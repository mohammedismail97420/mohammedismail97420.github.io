                        <p>Daily technological advancement, cherry topped by the COVID crisis, has, no doubt,
                            accelerated the transition of businesses to a virtual mode but HR in my mind is still a
                            function that is better imagined outside the 2-Dimensional digital screen!</p>

                        <p>A month or two ago, when the world was normal, most of the organizations preferred their
                            final interviews for any small or big role to be in person. There's a reason to this.
                            Physically, when you interact with a person, there's a lot more information which can be
                            gathered about the individual - be it through his/her body language, attention to details
                            for the objects in surrounding, facial expressions on any statement/questions/work
                            environment, ability to handle difficult/unprecedented situation etc etc. And this is true
                            for participants on the either end. Moreover, there has also been studies which shows that
                            the probability of a person panicking in a virtual interview - for reasons as small as poor
                            internet connection, inability to establish a comfort zone in the initial few minutes, fear
                            of the participant's environment/attire being judged etc. - is higher than in a physical
                            interaction. This is even higher in case of freshers hiring especially if the candidate is
                            not from some of the top metropolitan cities.
                        </p>

                        <p>Talking about the emotional health of employees, data says, a 24*7 home environment for work
                            is leading to a declining emotional health. Bonding with colleagues over daily meals,
                            celebrations, birthdays, anniversaries have drastically reduced and this leads to a
                            relationship which is only business-specific. Here again, employee engagement on a virtual
                            platform becomes little less effective than a physical activity/fun. Lastly, (and
                            unfortunately too) domestic issues between couples and divorce rates have started to show a
                            northwards trend!</p>

                        <p>These are some of the issues which gets a backseat in this rapid race of virtualization, but
                            definitely worth a thought. </p>
                        <p>Amidst all the positives of moving virtual, the biggest challenge would be to fill up for the
                            missing "emotional" touch or the "Human" aspect of Human Resource function. While the idea
