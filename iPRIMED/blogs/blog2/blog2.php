                    <div class="bcont">


                        <p>In my earlier post ( Tech Reskilling may not be the panacea:
                            https://www.linkedin.com/pulse/tech-reskilling-may-panacea-navin-kumar/), I had talked about
                            Entrepreneurship as the
                            top of the charts option for professionals experiencing mid life crisis and the fact that
                            being a first
                            time
                            entrepreneur at this stage in life is quite different from experiencing entrepreneurship in
                            the early/mid
                            20s! Before,
                            I start detailing this out, a few qualifiers/assumptions about the would be entrepreneur:
                        </p>

                        <ul class="ml-4">
                            <li>that you are experiencing life crisis and have worked in a Corporation till now</li>
                            <li>that we are talking about ventures in the non manufacturing (tertiary sector)</li>
                            <li>that we are talking about ventures which are mostly in the B2B space; while broad tenets
                                are the same
                                for B2C – it is difficult to get funding for a B2C idea for a first time entrepreneur
                                starting at this
                                stage in life!!<br>
                                Now, your idea/venture could be of 2 kinds:</li>
                            <li><b>The path breaking one </b>– as Peter Thiel in his book Zero to One, refers to them as
                                going from 0
                                to 1
                                i.e. <b>vertical progress</b>. Basically, doing new things. Examples – word processors
                                replacing type
                                writers, Uber etc.</li>
                            <li><b>The regular one</b> – Peter Thiel refers to these as going from 1 to n i.e.
                                <b>horizontal
                                    progress</b>.
                                This
                                could of 2 kinds - a) Localization of an idea that is working elsewhere – e.g Flipkart
                                modelled along
                                Amazon, Ola along Uber etc. b) Incremental progress – e.g. Outsourcing giving way to
                                Offshoring ;
                                Offshoring driven by automation visavis plain vanilla Offshoring etc..<br>
                                It may be obvious that the “ horizontal" ideas are easier to execute esp. with your
                                years of
                                experience and the maturity and enhanced perceptiveness of the mid life brain! However,
                                the
                                fundamental issues that plague most entrepreneurs is not going away for this category
                                either:
                            </li>

                            <li><b>Working Capital Management </b>– while most students of “Business” ( read MBAs) and
                                “Entrepreneurship”
                                know this but let me tell you that knowing is not enough! It is a very different thing
                                to experience
                                this on a daily/hourly basis esp. if you run a B2B business and your hard earned money
                                is not paid on
                                time by your customers and quite a lot of them may be in the Fortune 100/500 list! The
                                pressure of
                                running payroll every month and paying your suppliers gives shivers and keeps many awake
                                through many
                                nights! This one has to be experienced :) to understand the real ramifications –
                                however, a prepared
                                mind does much better than an unprepared one!</li>

                            <li><b>Regulations & Paperwork </b>– budget for this, especially in India and probably other
                                developing
                                countries. If you run a
                                private limited company then you’ll have to call upon spiritual/mythical powers for help
                                esp. if you are
                                not greasing
                                palms! This can suck most of your creative energy and while dealing with this, the fire
                                in your belly
                                may leave the
                                belly :). You’ll find it very difficult to come to terms with the world where people
                                like you, who have
                                given up a
                                career in the illustrious Corporate world and, are willing to spend their own money and
                                of course their
                                time are not
                                allowed to their bit. The reasons for this bizzare behavior are many and is best
                                discussed over a drink.
                                You’ll draw
                                solace in Lord Curzon’s famous lines “Round and round like the diurnal revolution of the
                                earth went the
                                file, stately,
                                solemn, sure and slow.."</li>
                            <li>Fund raise – if you are looking at raising early stage funding then think twice as
                                investors are shy
                                when it comes
                                to your category of entrepreneurs! You have to have this lined up before you jump in –
                                both for the
                                company
                                requirements and for your personal needs ( maintaining that life style to keep the home
                                front happy!) -
                                5 - 7 years is
                                a decent horizon in the Indian context<br>
                                But, there are aspects that are more pronounced for the “vertical” ideas and isn’t
                                explicit knowledge (
                                for most
                                people)! The key ones that come to mind:</li>

                            <li><b>Logic fails most of the time!</b> - the assumption is that you have risen the ranks
                                in the
                                Corporate world
                                and must be
                                good at planning and leveraging rationality to the fullest. You can break down a problem
                                and solve it
                                using your
                                logical mind. However, a logical analysis tends to fail most of the times esp. for
                                vertical ideas as the
                                variables are
                                many and quite often hidden/not understood ( this also happens for horizontal ideas, but
                                to a lesser
                                degree)</li>

                            <li><b>“Business" and “Intellect" has to marry in the entrepreneur</b> – this is extremely
                                crucial for
                                success
                                esp. for the
                                vertical ideas which are relatively ambiguous and the working model (and as a corollary,
                                the related
                                competencies) is
                                yet to emerge! By design, esp. for professionals from the Corporate world , this tends
                                to be “Either/Or”
                                and not an
                                “And” function – the ones who are good on the business side ( understanding; business
                                acquisition) are
                                typically not
                                good in the content/subject depth from an understanding and execution stand point. It is
                                very difficult
                                to go with a 2
                                in a box model on this one! This is true of a Bill Gates, Steve Jobs, Jeff Bezos etc.
                            </li>
                            <li>People – While ability to attract and retain good talent is an issue for horizontal
                                ideas ( esp. if
                                you don't have
                                money to throw i.e. if you are not funded!) this almost becomes an unsurmountable issue
                                for the vertical
                                ideas where
                                even throwing money doesn’t help at times, as the perceived risks are high! This is a
                                big dampener on
                                your ability to
                                scale the idea and esp. if the vertical ideas are on the social side and addressing the
                                BOP (Bottom of
                                Pyramid)
                                segment then life becomes even more difficult for the entrepreneur!
                                Now, armed with this knowledge, you can do a gap analysis visavis the required state and
                                where you stand
                                today (
                                remember, you are from the Corporate world and this should be easy!) Some of the
                                dimensions to consider
                                apart from
                                financial/business acumen are – thought leadership, strategic, relationships,
                                operations, team, content
                                etc. Finally,
                                skilling is all about plugging in your gaps! Apart from your natural disposition, the
                                nature and size of
                                your gaps
                                would also depend on whether you were preparing for this day/eventuality over the years
                                during your
                                Corporate stint (
                                and gathering rich learning/experience) or have been pushed into it due to one or many
                                factors – shifts
                                in the
                                industry/market, inability to break through the glass ceiling, you really love your boss
                                :) etc. Now,
                                the gaps have to
                                be plugged, right? Since, you have been a seasoned professional and have plugged many
                                gaps in the past,
                                there is a
                                tendency to believe that even this one shall get plugged! Be careful here. Do keep in
                                mind that the
                                world that you are
                                attempting is relatively new to you and more importantly, your ability to unlearn has
                                diminished over
                                the years! It is
                                proven that physiologically our fluid intelligence ( the one that gets measured on IQ
                                tests) starts to
                                plateau or go
                                down around the 35 years mark and the brain tends to fossilize as far as new learning is
                                concerned.
                                Besides there is
                                tendency to rationalise the world based on what you have experienced over the years! So
                                net net,
                                unlearning is going
                                to be difficult and slow and is a battle that you have to be careful to fight (
                                remember, only those
                                battles ought to
                                be picked up where you know that you are going to win!).
                                How do you deal with this? 2 options:</li>
                            <ol class="ml-4">
                                <li><b>Best option </b>is if you chose a venture where the requirement for success align
                                    with your
                                    current
                                    strengths, belief systems etc. and where the gap is low. Work to the strengths of
                                    the middle aged
                                    brain which is a brain that is more balanced in terms of left and right orientation,
                                    a brain that can
                                    see patterns easily and quickly and is better at making judgements, finding
                                    solutions and even
                                    regulating our emotions! Then work on plugging the gaps working with a mentor –
                                    someone who has been
                                    there done that and more importantly is able/keen to coach/mentor/train. Best to
                                    have the business and
                                    intellect married in you, before you jump in!</li>
                                <li>If this is not possible, then it is <b>recommended that you stay with “horizontal”
                                        ventures </b>and
                                    find
                                    a few like minded folks who can fill up the gaps! You have to be careful in this
                                    selection – <b>it is
                                        not people whom you know but people with whom you have worked in the past and
                                        are compatible as far
                                        as working together is concerned</b>. Now, this by design is difficult to find
                                    as the universe
                                    itself is
                                    quite small and the ones that you’s like to partner with may not be willing to make
                                    the plunge! The
                                    training/mentoring here will focus more on the application of the combined skills
                                    for the venture –
                                    remember, all of you are new to entrepreneurship!</li>
                            </ol>
                        </ul>
                        <p>And finally comes <b>EXECUTION – the bane of most ideas/entrepreneurs! </b>There are ideas
                            galore – but,
                            what
                            differentiates a winning idea from the ones that don’t is the ability of the entrepreneur to
                            execute the
                            idea. In your case, you may be a champion in execution in your current job but don’t let
                            that fool you –
                            the variables and the eco system is very different out here. As a start, imagine how you’ll
                            introduce
                            yourself to your potential customer/employee without your old visiting card which has some
                            fancy
                            designation at a fancy company. Second, there is no support structure of that fancy company
                            – and please
                            for heaven’s sake don’t underestimate this as you probably don’t know what you don’t know!
                            Refrain from
                            behaving like an Abhimanyu and attempting the “Chakravyuh” and dying in the process…
                            The above can take an eternity to accomplish! You can’t afford to lose valuable time as you
                            are already
                            late to the party. We can talk more about this offline – you know, for a consideration even
                            we/I can help
                            :)</p>
