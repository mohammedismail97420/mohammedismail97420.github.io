                            months instead?
                            How about 12
                            months of base education (the 3 Rs and some General Awareness) and 4 months spent learning
                            how to stay
                            skilled/informed always - the kind of behaviors/beliefs that needs to be acquired so that we
                            stay relevant
                            all the
                            time. This is the critical skill required in the VUCA world where things change rapidly and
                            where content
                            is available
                            abundantly. We'll save a lot of money and time which could be deployed to solve other
                            problems like
                            skills, poverty
                            etc.</p>

                        <p>The world of Skills & Employability is yet to be embraced by Tech (the way Silicon Valley
                            understands
                            it). This is
                            rather unfortunate! Agreed, it is a complex space but isn’t that where we need tech the
                            most? Or, should
                            tech only
                            focus on increasing asset utilization or helping distribute someone’s money to someone else
                            and in the
                            process
                            allowing the distributor to take a cut or transactions of similar nature? Then probably, we
                            need to re
                            classify the
                            status given to tech, taking into cognizance its limitations or more importantly the
                            limitations of the
                            ones who hold
                            the purse strings that can unleash this kind of tech!</p>

