                            <li>On driving a behavioral shift in the learner
                                Traditionally, the efficacy of Tech (without a human) in achieving the above three has
                                been quite poor.
                                Even now, there are myriads of challenges:</li>
                            <li>While there is considerable success in "Enablers Space" like testing & measurement,
                                machines used
                                for training etc. - not much is happening in the "Core" space. The latter is quite
                                complex and
                                difficult - however, this is where the need is the most, if we have to eradicate the
                                world from the
                                menace of "Unemployability"</li>
                            <li>Simulation of real scenarios where learners can actually execute the tasks is difficult
                                in a pure
                                play online environment. This is specially true for the "Non Services Trades" like
